#include "holberton.h"

/**
 * more_numbers - Print the numbers 0 to 14 ten times followed by new line.
 *
 * Return: Void
 */
void more_numbers(void)
{
	int x, y;

	for (x = 0; x < 10; x++)
	{
		for (y = 0; y < 15; y++)
		{
			if (y >= 10)
				_putchar(y / 10 + '0');
			_putchar(y % 10 + '0');
		}
		_putchar('\n');
	}
}
